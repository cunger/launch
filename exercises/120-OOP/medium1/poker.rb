require_relative 'card'

class PokerHand
  def initialize(cards)
    @cards = cards
  end

  def print
  end

  def evaluate
    case
    when royal_flush?     then 'Royal flush'
    when straight_flush?  then 'Straight flush'
    when four_of_a_kind?  then 'Four of a kind'
    when full_house?      then 'Full house'
    when flush?           then 'Flush'
    when straight?        then 'Straight'
    when three_of_a_kind? then 'Three of a kind'
    when two_pair?        then 'Two pair'
    when pair?            then 'Pair'
    else                       'High card'
    end
  end

  private

  def royal_flush?
    straight_flush? && any_rank?('Ace')
  end

  def straight_flush?
    straight? && flush?
  end

  def four_of_a_kind?
    tuples(4).count == 1
  end

  def full_house?
    three_of_a_kind? && pair?
  end

  def flush?
    Deck::SUITS.any? { |suit| all_suits? suit }
  end

  def straight?
    @cards.all? do |c1|
      @cards.any? do |c2|
        c2.rank_value == c1.rank_value + 1 || c2.rank_value == c1.rank_value - 1
      end
    end
  end

  def three_of_a_kind?
    tuples(3).count == 1
  end

  def two_pair?
    tuples(2).count == 2
  end

  def pair?
    tuples(2).count == 1
  end

  def all_suits?(suit)
    @cards.all? { |card| card.suit == suit }
  end

  def any_rank?(*ranks)
    ranks.all? { |r| @cards.any? { |card| card.rank == r } }
  end

  def tuples(n)
    cards_with_same_rank.select { |cards| cards.size == n }
  end

  def cards_with_same_rank
    ranks = @cards.map(&:rank).uniq
    ranks.map { |rank| @cards.select { |card| card.rank == rank } }
  end
end

