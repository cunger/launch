class CircularQueue
  def initialize(size)
    @size = size
    @buffer = []
  end

  def enqueue(element)
    @buffer << element
    @buffer.shift if @buffer.size > @size
    element
  end

  def dequeue
    nil if @buffer.empty?
    @buffer.shift
  end
end

